package java_2.hw;

public class Task1 {
    public static void main(String[] args) {

        byte tByte = 4;
        short tShort = 56;
        int tInt = 89;
        long tLong = 12121L;
        float tFloat = 4.733436f;
        double tDouble = 4.35544532d;
        char tChar = 'G';

        System.out.println("the byte primitive type - " + tByte);
        System.out.println("the short primitive type - " + tShort);
        System.out.println("the int primitive type - " + tInt);
        System.out.println("the long primitive type - " + tLong);
        System.out.println("the float primitive type - " + tFloat);
        System.out.println("the double primitive type - " + tDouble);
        System.out.println("the char primitive type - " + tChar);

        Byte oByte = 4;
        Short oShort = 56;
        Integer oInt = 89;
        Long oLong = 12121L;
        Float oFloat = 4.733436f;
        Double oDouble = 4.35544532d;
        Character oChar = 'G';

        System.out.println("the byte object type - " + oByte);
        System.out.println("the short object type - " + oShort);
        System.out.println("the int object type - " + oInt);
        System.out.println("the long object type - " + oLong);
        System.out.println("the float object type - " + oFloat);
        System.out.println("the double object type - " + oDouble);
        System.out.println("the char object type - " + oChar);

    }
}
