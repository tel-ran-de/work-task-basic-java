package java_2.practice;

public class PracticeTask1 {
    public static void main(String[] args) {
        byte tByte = 1;
        short tShort = 23;
        int tInt = 10;
        long tLong = 34999;
        float tFloat = 34999f;
        double tDouble = 0.34999d;
        char tChar = 'D';
        boolean tBoolean = true;

        System.out.println("the byte type - " + tByte);
        System.out.println("the short type - " + tShort);
        System.out.println("the int type - " + tInt);
        System.out.println("the long type - " + tLong);
        System.out.println("the float type - " + tFloat);
        System.out.println("the double type - " + tDouble);
        System.out.println("the char type - " + tChar);
        System.out.println("the boolean type - " + tBoolean);
    }
}
